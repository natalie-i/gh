""" 5. Написати функцію < fibonacci >, яка приймає один аргумент і виводить всі числа Фібоначчі, що не перевищують його.
"""
def fibonacci(a):
	e=[]
	b=0
	c=1
	d=1
	if a>0:
		while d<a+1:
			e.append(d)
			d=d+b
			b=c
			c=d
		print('Список чисел Фібоначчі, що не перевищують',a,':',e)
	else:
		print('Число повинно бути більше 0. Спробуйте ще раз')

	return(e)
fibonacci(int(input('Введіть число більше за 0:')))
