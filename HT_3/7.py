''' 7. Написати функцію, яка приймає на вхід список і підраховує кількість однакових елементів у ньому.
'''
def func_1(a):
	b={}
	for i in a:
		if i in b:
			b[i]+=1
		else:
			b[i]=1
	print('Кількість однакових елементів у списку', a,':',b)
	return()
func_1(input('Введіть через пробіл дані для створення списку:').split())
