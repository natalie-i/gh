''' 6. Вводиться число. Якщо це число додатне, знайти його квадрат, якщо від'ємне, збільшити його на 100,
якщо дорівнює 0, не змінювати.
'''
def numb(z):
	if (z*10)%10==0:
		a=int(z)
	else:
		a=z
	if a>0:
		b=a**2
		print('Ви ввели число більше 0, тож квадрат',a, 'дорівнює:',b)
	elif a<0:
		b=a+100
		print('Ви ввели число меньше 0, тож',a,'збільшене на 100 дорівнює:',b)
	elif a==0:
		print('Ви ввели 0, тож нічого не змінилось:',a)
	return(a)
numb(float(input('Введіть число:')))
